    private void tbXetNghiemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbXetNghiemMouseClicked
        int row = this.tbXetNghiem.getSelectedRow();
        maPhieuKhamChiTiet = this.tbXetNghiem.getModel().getValueAt(row,0).toString();
        try {
            PreparedStatement ps = DBConnection.ThucThiTruyVan("select * from phieuKhamChiTiet where "
                    + "maPhieuKhamChiTiet like '"+maPhieuKhamChiTiet+"'");
            ResultSet rs = ps.executeQuery();
            rs.next();
            this.txtMaXetNghiem.setText(rs.getString(1));
            this.txtMaBenhNhan.setText(rs.getString(2));
            //this.txtMaNhanVien.setText(maNhanVien);
            this.txtNgayKham.setText(rs.getString(4));
            this.txtChiSoSucKhoe.setText(rs.getString(5));
            this.txtKetLuan.setText(rs.getString(6));
            this.txtYeuCau.setText(rs.getString(7));
            this.txtMaPhieuKham.setText(rs.getString(8));
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e, "Lỗi !", 0);
        }
    }//GEN-LAST:event_tbXetNghiemMouseClicked

