    private void btGenerateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGenerateActionPerformed
        int numXetNghiem=0;
        PreparedStatement ps = DBConnection.ThucThiTruyVan("select * from phieukhamchitiet");
        try {
            ResultSet rs = ps.executeQuery();
            while (rs.next()) numXetNghiem++;
            numXetNghiem++;
            this.txtMaXetNghiem.setText(String.valueOf(numXetNghiem));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex, "Lỗi !",0);
        }
    }//GEN-LAST:event_btGenerateActionPerformed

