    public void loadData(String maNV) throws SQLException{
        Model.QuanLyNhanVien qlNhanVien = new Model.QuanLyNhanVien();
        this.setNhanVien(qlNhanVien.timKiemMaNhanVien(maNV));
        this.jLabelAddTenNguoiDangNhap.setText(this.getNhanVien().getHoTen());
    }
    public void loadCombobox() throws SQLException{
        Model.QuanLyNhanVien qlNhanVien = new Model.QuanLyNhanVien();
        ArrayList<Controller.NhanVien> lstBacSi = new ArrayList<>();
        lstBacSi = qlNhanVien.findBacSi();
        for(Controller.NhanVien bacSi:lstBacSi){
            String tenBS_Ma = bacSi.getHoTen() + "_" + bacSi.getMaNhanVien();
            this.cbxTenBacSi.addItem(tenBS_Ma);
            //this.cbxTenBacSi.setSelectedItem(tenBS_Ma);
        }
    }
    
