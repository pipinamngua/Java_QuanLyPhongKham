    private void btnAddTaoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddTaoMouseClicked
        try {
            // TODO add your handling code here:
            String maBenhNhan = txtAddMaBenhNhan.getText();
            String tenBN = txtAddHoTen.getText();
            String gioiTinh = cbxAddGioiTinh.getSelectedItem().toString();
            String NS = txtAddNgaySinh.getText();
            SimpleDateFormat formatter = new SimpleDateFormat("dd-MM-yyyy");
            Date ngaySinh = formatter.parse(NS);
            String ngheNghiep = txtNgheNghiep.getText();
            int tinhTrangHonNhan;
            if(cbxAddTinhTrangHonNhan.getSelectedItem().toString().equals("Độc thân"))
                tinhTrangHonNhan = 0;
            else
                tinhTrangHonNhan = 1;
            String diaChi = txtAddDiaChi.getText();
            String tieuSuBenhAn = txtAddTienSuBenhAn.getText();
            String soDienThoai = txtAddSoDienThoai.getText();
            String soCMND = txtAddCMND.getText();
            Controller.BenhNhan benhNhan = new Controller.BenhNhan(maBenhNhan, tenBN, gioiTinh, ngaySinh, ngheNghiep, tinhTrangHonNhan, diaChi, tieuSuBenhAn, soDienThoai, soCMND);
            QlBenhNhan qlBenhNhan = new QlBenhNhan();
            Boolean rs2, rs = qlBenhNhan.themMoiBenhNhan(benhNhan);
            String maPhieuKham = txtAddMaPhieuKham.getText();
            String lsDoKham = txtAddLyDoKham.getText();
            String ghiChu = txtAddGhiChu.getText();
            String tenBacSi = cbxTenBacSi.getSelectedItem().toString();
            Controller.QlPhieuKham qlPhieuKham = new Controller.QlPhieuKham();
            Controller.PhieuKham pk = new Controller.PhieuKham(maPhieuKham, this.getNhanVien().getMaNhanVien(), maBenhNhan, lsDoKham, ghiChu, tenBacSi, 0.0);
            rs2 = qlPhieuKham.themMoiPhieuKham(pk);
            if(rs2){
                JOptionPane.showMessageDialog(rootPane, "Lưu thông tin bệnh nhân thành công");
                //FrXemThongTinBenhNhan thongTin = new FrXemThongTinBenhNhan(txtAddMaBenhNhan.getText(), txtAddMaPhieuKham.getText(), cbxTenBacSi.getSelectedItem().toString(), this.getNhanVien().getMaNhanVien());
            }
            else
            {
                JOptionPane.showMessageDialog(rootPane, "Lưu thông tin bệnh nhân không thành công");

            }
            this.setVisible(false);
            FrKhamTongQuat frTQ = new FrKhamTongQuat(this.getNhanVien().getMaNhanVien());
            frTQ.show();
            
        } catch (ParseException ex) {
            Logger.getLogger(FrTiepNhanBenhNhanMoi.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(FrTiepNhanBenhNhanMoi.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddTaoMouseClicked

